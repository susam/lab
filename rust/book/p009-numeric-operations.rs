fn main() {
    let a = 5 + 10;
    let b = 95.5 - 4.3;
    let c = 4 * 38;

    println!("a: {a}");
    println!("b: {b}");
    println!("c: {c}");

    let d = 10.0 / 3.0;
    let e = 10 / 3;
    let f = -10 / 3;
    println!("d: {d}"); // 3.3333333333333335
    println!("e: {e}"); // 3
    println!("f: {f}"); // -3

    let h = 10 % 3;
    let i = -10 % 3;
    println!("h: {h}"); // 1
    println!("i: {i}"); // -1
}
